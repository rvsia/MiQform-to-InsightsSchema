    "DialogFieldTextBox": "text-field",
    "DialogFieldRadioButton": "radio-field",
    "DialogFieldCheckBox": "checkbox-field",
    "DialogFieldTextAreaBox": "textarea-field",
    "DialogFieldDropDownList": "select-field",
